package ru.groza1337;

/**
 * Перечисление типов углов.
 */
public enum AngleType {
    ACUTE,      // Острый угол (< 90°)
    RIGHT,      // Прямой угол (90°)
    OBTUSE,     // Тупой угол (> 90° и < 180°)
    STRAIGHT,   // Развернутый угол (180°)
    REFLEX,     // Рефлексный угол (> 180° и < 360°)
    FULL        // Полный круг (360°)
}
